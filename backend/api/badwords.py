def CheckArrayOfWords(array, separateText):
    pos = -1
    words = ''
    for item in separateText:
        for forbidWord in array:
            pos = str(item).find(forbidWord)
            if pos == 0:
                words += item + ', '
                break
    return words


def CheckArrayOfText(array, text):
    pos = -1
    words = ''
    for forbidWord in array:
        pos = str(text).find(forbidWord)
        if pos >= 0:
            words += forbidWord + ', '
    return words


